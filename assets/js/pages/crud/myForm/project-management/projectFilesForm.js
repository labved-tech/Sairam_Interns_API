'use strict';

/* Class definition */
const ProjectManagementCRUD = (function () {
    const _buttonSpinnerClasses = 'spinner spinner-right spinner-white pr-15';

    // Date Picker
    let arrows;
    if (KTUtil.isRTL()) {
        arrows = {
            leftArrow: '<i class="la la-angle-right"></i>',
            rightArrow: '<i class="la la-angle-left"></i>',
        };
    } else {
        arrows = {
            leftArrow: '<i class="la la-angle-left"></i>',
            rightArrow: '<i class="la la-angle-right"></i>',
        };
    }
    /*   Private functions */
    const _createprojectFilesForm = function () {

        // Getting Document related information
        const createprojectFilesForm = KTUtil.getById('createprojectFilesForm');
        const pfFormSubmitButton = KTUtil.getById('pfFormSubmitButton');
        const pffileName = KTUtil.getById('pffileName');
        const pffileType = KTUtil.getById('pffileType');
        const pfsubject = KTUtil.getById('pfsubject');
        const pfdescription = KTUtil.getById('pfdescription');
        const pfdateadded = KTUtil.getById('pfdateadded');
        const pfprojectId = KTUtil.getById('pfprojectId');
        const pfuserId = KTUtil.getById('pfuserId');
        const pfexternal = KTUtil.getById('pfexternal');
        const pfthumbnailLink = KTUtil.getById('pfthumbnailLink');

        //Intialize

        $('#pfdateadded').datepicker({
            rtl: KTUtil.isRTL(),
            todayBtn: 'linked',
            clearBtn: true,
            todayHighlight: true,
            orientation: 'bottom left',
            templates: arrows,
        });

        // Description

        $(pfdescription).summernote({
            height: 400,
            tabsize: 2,
        });

        // Return Form
        if (!createprojectFilesForm) {
            return;
        }

        // Validation
        const fv = FormValidation.formValidation(createprojectFilesForm, {
            fields: {
                pffileName: {
                    validators: {
                        notEmpty: {
                            message: 'FileName is required',
                        },
                    },
                },
                pffileType: {
                    validators: {
                        notEmpty: {
                            message: 'FileType is required',
                        },
                    },
                },
                pfsubject: {
                    validators: {
                        notEmpty: {
                            message: 'Subject is required',
                        },
                    },
                },
                pfdescription: {
                    validators: {
                        callback: {
                            message: 'The content is required and cannot be empty',
                            callback: function (input) {
                                const code = $('[name="pfdescription"]').summernote('code');
                                // <p><br></p> is code generated by Summernote for empty content
                                return (code !== '' && code !== '<p><br></p>');
                            }
                        },
                    },
                },
                pfdateadded: {
                    validators: {
                        notEmpty: {
                            message: 'Date added is required',
                        },
                    },
                },
                pfprojectId: {
                    validators: {
                        notEmpty: {
                            message: 'ProjectId is required',
                        },
                    },
                },
                pfuserId: {
                    validators: {
                        notEmpty: {
                            message: 'UserId is required',
                        },
                    },
                },
                pfexternal: {
                    validators: {
                        notEmpty: {
                            message: 'External is required',
                        },
                    },
                },
                pfthumbnailLink: {
                    validators: {
                        notEmpty: {
                            message: 'ThumbnailLink is required',
                        },
                    },
                },
            },

            plugins: {
                //Learn more: https://formvalidation.io/guide/plugins
                trigger: new FormValidation.plugins.Trigger(),
                // Bootstrap Framework Integration
                bootstrap: new FormValidation.plugins.Bootstrap(),
                // Validate fields when clicking the Submit button
                pfFormSubmitButton: new FormValidation.plugins.SubmitButton(),
                // Submit the form when all fields are valid
                //defaultSubmit: new FormValidation.plugins.DefaultSubmit(),
            },
        })
            .on('core.form.valid', function () {
                // Show loading state on button
                KTUtil.btnWait(pfFormSubmitButton, _buttonSpinnerClasses, 'Please wait');
                console.log(`Value:${pffileName.value}`);

                // Accessing Restful API
                axios({
                    method: 'post',
                    url: `${HOST_URL}/api/v1/project-management/projecttaskfiles`,
                    data: {
                        fileName: pffileName.value,
                        fileType: pffileType.value,
                        subject: pfsubject.value,
                        description: pfdescription.value,
                        dateadded: pfdateadded.value,
                        projectId: pfprojectId.value,
                        taskId: pftaskId.value,
                        userId: pfuserId.value,
                        external: pfexternal.value,
                        thumbnailLink: pfthumbnailLink.value,
                    },

                }).then(function (res) {
                    // Return valid JSON
                    // Release button
                    KTUtil.btnRelease(pfFormSubmitButton);
                    console.log(res);

                    // TOASTR EXAMPLE

                    toastr.options = {
                        "closeButton": false,
                        "debug": false,
                        "newestOnTop": true,
                        "progressBar": false,
                        "positionClass": "toast-top-right",
                        "preventDuplicates": false,
                        "onclick": null,
                        "showDuration": "300",
                        "hideDuration": "1000",
                        "timeOut": "5000",
                        "extendedTimeOut": "1000",
                        "showEasing": "swing",
                        "hideEasing": "linear",
                        "showMethod": "fadeIn",
                        "hideMethod": "fadeOut"
                    };

                    if (res.data.status == 'success') {
                        toastr.success(`${res.data.message}`, `${res.data.status}`)
                    } else if (res.data.status == 'error') {
                        toastr.error(`${res.data.message}`, `${res.data.status}`)
                    }
                });

            })
            .on('core.form.invalid', function () {
                console.log('Something went wrong!!');

            });

    };

    return {
        // public functions
        init: function () {
            _createprojectFilesForm();
        },
    };
})();

jQuery(document).ready(function () {
    ProjectManagementCRUD.init();
});