'use strict';

// Class definition
const FarmCRUD = (function () {
    const _buttonSpinnerClasses = 'spinner spinner-right spinner-white pr-15';

    const _createProjectNotes = function () {

        const createProjectNotesForm = KTUtil.getById('createProjectNotesForm');
        const createProjectNotesFormSubmitButton = KTUtil.getById('createProjectNotesFormSubmitButton');
        const pnProjectId = KTUtil.getById('pnProjectId');
        const pnUserId = KTUtil.getById('pnUserId');
        const pnContent = KTUtil.getById('pnContent');

        // Initialise
        $(pnContent).summernote({
            height: 400,
            tabsize: 2,
        });

        if (!createProjectNotesForm) {
            return;
        }

        FormValidation.formValidation(createProjectNotesForm, {
            fields: {
                pnProjectId: {
                    validators: {
                        notEmpty: {
                            message: 'ProjectId is required',
                        },
                    },
                },
                pnUserId: {
                    validators: {
                        notEmpty: {
                            message: 'UserId is required',
                        },
                    },
                },
                pnContent: {
                    validators: {
                        callback: {
                            message: 'The content is required and cannot be empty',
                            callback: function (input) {
                                const code = $('[name="pnContent"]').summernote('code');
                                // <p><br></p> is code generated by Summernote for empty content
                                return (code !== '' && code !== '<p><br></p>');
                            }
                        },
                    },
                },
            },
            plugins: {
                //Learn more: https://formvalidation.io/guide/plugins
                trigger: new FormValidation.plugins.Trigger(),
                // Bootstrap framework Integration
                bootstrap: new FormValidation.plugins.Bootstrap(),
                // Validate fields when clicking the Submit button
                createProjectNotesFormSubmitButton: new FormValidation.plugins.SubmitButton(),
                // Submit the form when all fields are valid
                //defaultSubmit: new FormValidation.plugins.DefaultSubmit(),
            },
        }).on('core.form.valid', function () {
            KTUtil.btnWait(createProjectNotesFormSubmitButton, _buttonSpinnerClasses, 'Please wait');

            // Accessing Restful API
            pnShareable.value = (pnShareable.value == 'on') ? true : false

            axios({
                method: 'Post',
                url: `${HOST_URL}/api/v1/project/notes/`,
                data: {
                    _projectId: pnProjectId.value,
                    _userId: pnUserId.value,
                    content: $('#pnContent').summernote('code'),

                },
            }).then(function (res) {
                KTUtil.btnRelease(createProjectNotesFormSubmitButton);
                console.log(res);

                // TOASTR EXAMPLE
                toastr.options = {
                    "closeButton": false,
                    "debug": false,
                    "newestOnTop": true,
                    "progressBar": false,
                    "positionClass": "toast-top-right",
                    "preventDuplicates": false,
                    "onclick": null,
                    "showDuration": "300",
                    "hideDuration": "1000",
                    "timeOut": "5000",
                    "extendedTimeOut": "1000",
                    "showEasing": "swing",
                    "hideEasing": "linear",
                    "showMethod": "fadeIn",
                    "hideMethod": "fadeOut"
                };

                if (res.data.status == 'success') {
                    toastr.success(`${res.data.message}`, `${res.data.status}`)
                } else if (res.data.status == 'error') {
                    toastr.error(`${res.data.message}`, `${res.data.status}`)
                }
            });

        })
            .on('core.form.invalid', function () {
                console.log('Something went wrong!!');

            });

    };

    return {
        // public functions
        init: function () {
            _createProjectNotes();
        },
    };
})();

jQuery(document).ready(function () {
    FarmCRUD.init();
});
